package com.xpn.xwiki.plugin.charts.exceptions;

public class InvalidParamException extends ParamException {

	public InvalidParamException() {
		super();
	}

	public InvalidParamException(String arg0) {
		super(arg0);
	}

	public InvalidParamException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidParamException(Throwable arg0) {
		super(arg0);
	}
}
