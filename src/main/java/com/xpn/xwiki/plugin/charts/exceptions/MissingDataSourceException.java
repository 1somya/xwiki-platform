package com.xpn.xwiki.plugin.charts.exceptions;

public class MissingDataSourceException extends DataSourceException {
    public MissingDataSourceException() {
        super();
    }

    public MissingDataSourceException(String arg0) {
        super(arg0);
    }

    public MissingDataSourceException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public MissingDataSourceException(Throwable arg0) {
        super(arg0);
    }

    private static final long serialVersionUID = -3378540337805442568L;
}
