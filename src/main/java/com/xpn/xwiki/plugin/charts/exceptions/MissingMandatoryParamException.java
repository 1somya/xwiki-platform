package com.xpn.xwiki.plugin.charts.exceptions;

public class MissingMandatoryParamException extends ParamException {

	public MissingMandatoryParamException() {
		super();
	}

	public MissingMandatoryParamException(String arg0) {
		super(arg0);
	}

	public MissingMandatoryParamException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MissingMandatoryParamException(Throwable arg0) {
		super(arg0);
	}

}
